window.SP_t = function (val, lang) {
  const l = lang || window.SP_LANG || 'en';
  if (val == null) return '';
  if (typeof val === 'object' && (val.pt || val.en)) return val[l] || val.en || val.pt || '';
  return String(val);
};

(function initLang() {
  const stored = localStorage.getItem('sp_lang');
  window.SP_LANG = stored === 'pt' || stored === 'en' ? stored : 'en';
})();

window.SP_setLang = function (lang) {
  if (lang !== 'pt' && lang !== 'en') return;
  window.SP_LANG = lang;
  localStorage.setItem('sp_lang', lang);
  document.documentElement.lang = lang === 'pt' ? 'pt-BR' : 'en';

  const dict = window.SP_I18N[lang];
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] != null) el.innerHTML = dict[key];
  });
  document.querySelectorAll('[data-i18n-attr]').forEach((el) => {
    const spec = el.getAttribute('data-i18n-attr');
    spec.split(';').forEach((pair) => {
      const [attr, key] = pair.split(':').map((s) => s.trim());
      if (attr && key && dict[key] != null) el.setAttribute(attr, dict[key]);
    });
  });

  const titleEl = document.querySelector('title');
  if (titleEl && dict.pageTitle) titleEl.textContent = dict.pageTitle;

  const toggle = document.querySelector('.lang-toggle');
  if (toggle) {
    const active = toggle.querySelector('.lang-active');
    const inactive = toggle.querySelector('.lang-inactive');
    if (active && inactive) {
      active.textContent = lang === 'en' ? 'EN' : 'PT';
      inactive.textContent = lang === 'en' ? 'PT' : 'EN';
    }
    toggle.dataset.lang = lang;
  }

  if (typeof window.SP_renderTeam === 'function') window.SP_renderTeam();
  if (typeof window.SP_renderCases === 'function') window.SP_renderCases();
  if (typeof window.SP_updateChips === 'function') window.SP_updateChips();
};

document.addEventListener('DOMContentLoaded', () => {
  window.SP_setLang(window.SP_LANG);
});
